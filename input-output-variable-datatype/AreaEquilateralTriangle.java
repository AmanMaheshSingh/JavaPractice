import java.util.*;

public class AreaEquilateralTriangle{
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter Side Of Equilateral Triangle");
        float a = sc.nextFloat();
        float Area=0.43f*a*a;
        System.out.println("The Area Of Equilateral Triangle with Given Side : "+a+" is = "+Area);
    }
}